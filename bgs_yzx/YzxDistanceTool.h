template<typename T>
static inline size_t YZXpopcount(T x)
{
	size_t nBytes = sizeof(T);
	size_t res = 0;
	for (size_t i = 0; i < nBytes; ++i)
	{
		res += YZXpopcount_LUT8[(uchar)(x >> (i * 8))];
	}
	return res;
}